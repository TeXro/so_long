#include "so_long.h"

void	write_num(int n)
{
	char	c;

	if (n == -2147483648)
	{
		write(1, "-", 1);
		write(1, "2", 1);
		n = 147483648;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
		write_num(n / 10);
	c = (n % 10) + '0';
	write(1, &c, 1);
}
